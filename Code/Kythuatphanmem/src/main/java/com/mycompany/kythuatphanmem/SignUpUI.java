    private boolean isAccountExist(String username) {
        try {
            File xmlFile = new File("src/main/java/com/mycompany/kythuatphanmem/accounts.xml");

            DocumentBuilderFactory dbFactory = DocumentBuilderFactory.newInstance();
            DocumentBuilder dBuilder = dbFactory.newDocumentBuilder();
            org.w3c.dom.Document doc = dBuilder.parse(xmlFile);
            doc.getDocumentElement().normalize();

            org.w3c.dom.NodeList nodeList = doc.getElementsByTagName("account");

            for (int i = 0; i < nodeList.getLength(); i++) {
                org.w3c.dom.Element element = (org.w3c.dom.Element) nodeList.item(i);
                String xmlUsername = element.getElementsByTagName("username").item(0).getTextContent();
                if (username.equals(xmlUsername)) {
                    return true;
                }
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
        return false;
    }

    private boolean addAccountToXML(String username, String password) {
        try {
            File xmlFile = new File("src/main/java/com/mycompany/kythuatphanmem/accounts.xml");

            DocumentBuilderFactory dbFactory = DocumentBuilderFactory.newInstance();
            DocumentBuilder dBuilder = dbFactory.newDocumentBuilder();
            org.w3c.dom.Document doc = dBuilder.parse(xmlFile);

            org.w3c.dom.Element root = doc.getDocumentElement();

            org.w3c.dom.Element newAccount = doc.createElement("account");

            org.w3c.dom.Element userElem = doc.createElement("username");
            userElem.setTextContent(username);
            newAccount.appendChild(userElem);

            org.w3c.dom.Element passElem = doc.createElement("password");
            passElem.setTextContent(password);
            newAccount.appendChild(passElem);

            root.appendChild(newAccount);

            // Ghi lại file XML
            javax.xml.transform.TransformerFactory transformerFactory = javax.xml.transform.TransformerFactory.newInstance();
            javax.xml.transform.Transformer transformer = transformerFactory.newTransformer();
            javax.xml.transform.dom.DOMSource source = new javax.xml.transform.dom.DOMSource(doc);
            javax.xml.transform.stream.StreamResult result = new javax.xml.transform.stream.StreamResult(xmlFile);
            transformer.transform(source, result);

            return true;
        } catch (Exception e) {
            e.printStackTrace();
            return false;
        }
    }

