    private void signUpButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_signUpButtonActionPerformed
        // TODO add your handling code here:
        String username = txtUserName.getText().trim();
        String password = txtPassword.getText().trim();

            if (username.isEmpty() || password.isEmpty()) {
                LoginDialog.showMessageDialog(this, "User name hoặc password không được bỏ trống!");
                return;
            }

            if (isAccountExist(username)) {
                LoginDialog.showMessageDialog(this, "Tài khoản đã tồn tại!");
                return;
            }

            if (addAccountToXML(username, password)) {
                LoginDialog.showMessageDialog(this, "Đã tạo tài khoản thành công!");
                signUpButton.setText("Login");
            } else {
                LoginDialog.showMessageDialog(this, "Lỗi khi đăng kí tài khoản!");
            }
            
    }//GEN-LAST:event_signUpButtonActionPerformed

